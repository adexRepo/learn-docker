# -------------------------- Melihat logs container -------------------------- #
# Melihat yang sudah ada
# Format : docker container logs containerId/containerName
# Contoh :
    docker container logs contohRedis

# Melihat secara realtime / atau selalu menunggu yang baru
# Format : docker container logs -f containerId/containerName
# Contoh : 
    docker container logs -f contohRedis
